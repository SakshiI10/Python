''' 
Literals are notations for constant values of some in-built data types.
1) Numeric Literals are of 3 types:
    i) Integer literals
    ii) Floating point literals
    iii) Imaginery literals
    There are no complex literals in Python.
    Numeric Literals don't include sign.
'''

#i) Integer Literals:
print(12_345)
print(0x123)
print(0o50)
print(0b1011001)

#ii) Floating Point Literals
print(123.456)
print(12_34.45_67)
print(0.01)
print(0.001)
print(0.0001)
print(0.00001)
print(0.000001)
print(1.23e4)
print(0e0)
print(77e05)

#iii) Imaginery Literals
print(1.23j)
print(5j)
print(0.02j)
print(.01j)
print(1.23e3j)
