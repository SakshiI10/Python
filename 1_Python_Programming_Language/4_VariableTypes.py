''' 
1) Numbers: 
   Python supports 3 types of numbers: 
      i) Integers or fixed point numbers
      ii) Reals or Floating point numbers
      iii) Complex numbers
'''
a=10
b=20
c=a+b
print(c)

price=25.5
qty=7
amount=price*qty
print(amount)

c1=2+3j
c2=5-2j
c3=c2+c1
print(c3)
print(c1*c2)

