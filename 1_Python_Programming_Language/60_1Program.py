# Performing iterations over sets.
student_id={1,2,3,4,5}
print("student id: ", student_id)
for n in student_id:
    print(n, end='\n')
    
vowel_letters={'m', 'k', 'c', 'l'}
print("\n Vowel Letters: ", vowel_letters)
for n in vowel_letters:
    print(n, end='\n')
    
mixed_set={"Hello", 6, -2, "Bye"}
print("\n Set of mixed data types: ", mixed_set)
for n in mixed_set:
    print(n, end='\n')