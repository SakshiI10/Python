'''
3. There are different types of inheritance supported by Python:
a. Single Inheritance: A subclass inherits from only one parent class. This is the most common and straightforward type.

b. Multiple Inheritance: A subclass inherits from multiple parent classes. This can be trickier to manage as it can lead to ambiguity if both parent classes have methods with the same name.

c. Multilevel Inheritance: A subclass inherits from another subclass, which in turn inherits from a base class. This creates a chain of inheritance.

d. Hierarchical Inheritance: Multiple subclasses inherit from a single base class.'''